def get_bias_correction(hour: int, dow: int) -> float:
    """Get correction value for hour and day of week."""
    return -BIAS_TABLE.get((hour, dow), 0)


def apply_bias_correction(predictions: pd.DataFrame,
                          timestamp_col: str = 'timestamp',
                          prediction_col: str = 'predicted_demand') -> pd.DataFrame:
    """Apply bias correction to predictions."""
    df = predictions.copy()
    ts = pd.to_datetime(df[timestamp_col])
    
    df['bias_correction'] = [get_bias_correction(t.hour, t.dayofweek) for t in ts]
    df['corrected_demand'] = df[prediction_col] + df['bias_correction']
    
    return df
